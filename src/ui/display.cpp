void setupDisplay() {
  if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) {
    Serial.println(F("SSD1306 allocation failed"));
    while (1);
  }
  display.clearDisplay();
  display.setTextSize(1);
  display.setTextColor(SSD1306_WHITE);
  display.display();
}

void updateDisplay(int16_t leftSpeed, int16_t rightSpeed, uint8_t channel, bool isUpsideDown) {
  display.clearDisplay();
  
  display.setTextSize(1);
  display.setTextColor(WHITE);
  
  display.drawXBitmap(2, 2, image_download_bits, 18, 13, WHITE);
  
  display.setCursor(30, 4);
  display.print("Info screen:");

  char buf[20];
  
  display.setCursor(2, 20);
  sprintf(buf, "Channel: %d", channel);
  display.print(buf);

  int move = (abs(leftSpeed) + abs(rightSpeed)) / 2;
  display.setCursor(2, 28);
  sprintf(buf, "Move: %d", move);
  display.print(buf);

  display.setCursor(2, 36);
  display.print("Weapon: 55");
  display.setCursor(2, 44);
  display.print("Li-ion: 90");
  display.setCursor(2, 52);
  display.print("Lipo: 75");
  
  display.drawRect(0, 0, 128, 17, WHITE);
  display.drawLine(21, 0, 21, 15, WHITE);
  
  if (isUpsideDown) {
    display.drawXBitmap(88, 26, image_download_1_flipped_bits, 25, 27, WHITE);
  } else {
    display.drawXBitmap(88, 26, image_download_1_bits, 25, 27, WHITE);
  }

  display.display();
}

void updateDisplayMessage(const char* message) {
  display.clearDisplay();
  display.setCursor(30, 30);
  display.print(message);
  display.display();
}
